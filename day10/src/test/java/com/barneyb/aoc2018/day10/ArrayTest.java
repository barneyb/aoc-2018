package com.barneyb.aoc2018.day10;

import org.junit.Test;

import static com.barneyb.aoc2018.day10.Day10Test.exampleParticles;
import static org.junit.Assert.assertEquals;

public class ArrayTest {

    @Test
    public void ticks() {
        Array a = new Array(exampleParticles());
        assertEquals(
                "........#.............\n" +
                "................#.....\n" +
                ".........#.#..#.......\n" +
                "......................\n" +
                "#..........#.#.......#\n" +
                "...............#......\n" +
                "....#.................\n" +
                "..#.#....#............\n" +
                ".......#..............\n" +
                "......#...............\n" +
                "...#...#.#...#........\n" +
                "....#..#..#.........#.\n" +
                ".......#..............\n" +
                "...........#..#.......\n" +
                "#...........#.........\n" +
                "...#.......#..........",
                a.toString());
        a.tick();
        assertEquals(
                "........#....#....\n" +
                "......#.....#.....\n" +
                "#.........#......#\n" +
                "..................\n" +
                "....#.............\n" +
                "..##.........#....\n" +
                "....#.#...........\n" +
                "...##.##..#.......\n" +
                "......#.#.........\n" +
                "......#...#.....#.\n" +
                "#...........#.....\n" +
                "..#.....#.#.......",
                a.toString());
        a.tick();
        assertEquals(
                "..........#...\n" +
                "#..#...####..#\n" +
                "..............\n" +
                "....#....#....\n" +
                "..#.#.........\n" +
                "...#...#......\n" +
                "...#..#..#.#..\n" +
                "#....#.#......\n" +
                ".#...#...##.#.\n" +
                "....#.........",
                a.toString());
        a.tick();
        assertEquals(
                "#...#..###\n" +
                "#...#...#.\n" +
                "#...#...#.\n" +
                "#####...#.\n" +
                "#...#...#.\n" +
                "#...#...#.\n" +
                "#...#...#.\n" +
                "#...#..###",
                a.toString());
        a.tick();
        assertEquals(
                "........#....\n" +
                "....##...#.#.\n" +
                "..#.....#..#.\n" +
                ".#..##.##.#..\n" +
                "...##.#....#.\n" +
                ".......#....#\n" +
                "..........#..\n" +
                "#......#...#.\n" +
                ".#.....##....\n" +
                "...........#.\n" +
                "...........#.",
                a.toString());
    }

}